# Perintah masukan angka
num = int(input("Masukan angkanya:"))
# Jika angka yang di input lebih dari 10 maka:
if num > 10:
    # Tampilkan "Angka yang kamu masukan lebih dari 10"
    print("Angka yang kamu masukan lebih dari 10")
# Jika angka yang di input sama dengan 10 maka:
elif num == 10:
    # Tampilkan "Angka yang kamu masukan sama dengan 10"
    print("Angka yang kamu masukan sama dengan 10")
# Jika angka yang di input kurang dari 10 maka:
else:
    # Tampilkan "Angka yang kamu masukan kurang dari 10"
    print("Angka yang kamu masukan kurang dari 10")